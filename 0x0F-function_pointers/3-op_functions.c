#include "3-calc.h"

/**
* op_add - sum of two integers
* @a: first interger
* @b: second integer
* Return: sum of integers
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - difference of two integers
* @a: first interger
* @b: second integer
* Return: sub of integers
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - multiple of two integers
* @a: first interger
* @b: second integer
* Return: multiple of integers
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - division of two integers
* @a: first interger
* @b: second integer
* Return: division of integers
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - modulo of two integers
* @a: first interger
* @b: second integer
* Return: remainder of integers
*/

int op_mod(int a, int b)
{
	return (a % b);
}
